// 상담 기록 요약 평문화(세션 66) — AI 요약이 과거에 마크다운(**굵게**, ## 제목)으로
// 생성된 기록이 남아 있다. 어떤 화면(케어로그·전자차트)도 마크다운을 렌더하지 않으므로
// 표시·복사 시점에 기호를 걷어낸다. 신규 요약은 프롬프트에서 평문([라벨] 구조)으로 생성.
// HTML 문자열에도 안전: 태그를 건드리지 않고 텍스트 마커만 제거한다.
export function stripMarkdownMarkers(s: string): string {
  if (!s) return s;
  return (
    s
      // **굵게** 마커 제거
      .replace(/\*\*/g, "")
      // 줄 머리(또는 태그 직후)의 # 제목 마커 제거 — "## 치과 상담 기록 요약" → "치과 상담 기록 요약"
      .replace(/(^|>|\n)(\s*)#{1,6}\s+/g, "$1$2")
  );
}
